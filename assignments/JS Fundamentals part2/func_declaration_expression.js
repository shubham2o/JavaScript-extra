function percentageOfWorld1(chinaPopulation) {
    let population = 7900;
    let percentage = (chinaPopulation / population) * 100;
    return percentage;
}

console.log(`${percentageOfWorld1(10)}`);
console.log(`${percentageOfWorld1(1441)}`);
console.log(`${percentageOfWorld1(332)}`);



